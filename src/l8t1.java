import java.io.*;
import java.util.StringTokenizer;

public class l8t1 {
    public static int[][] f;

    public static void main(String[] args) {
        QReader in = new QReader();
        QWriter out = new QWriter();
        int n = in.nextInt();
        int m = in.nextInt();
        int q = in.nextInt();
        f = new int[n + 1][n + 1];
        for (int i = 1; i <= n; i++)
            for (int j = 1; j <= n; j++) f[i][j] = 0;
        int a, b;
        for (int i = 0; i < m; i++) {
            a = in.nextInt();
            b = in.nextInt();
            f[b][a] = 1;
            f[a][b] = 1;
        }
        for (int i = 0; i < q; i++) {
            a = in.nextInt();
            b = in.nextInt();
            if (f[a][b] == 1) out.println("Yes");
            else out.println("No");
        }
        out.close();
    }
}

